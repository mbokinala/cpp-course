#include <iostream>

using namespace std;
// Variables

main() {
    int a = 4;

    cout << a << endl;
    cout << &a << endl;

    a = 10;

    cout << a << endl;
    cout << &a;

    /*
        1. Variables can't have the same name
        2. Variables can't start with numbers
        3. Spaces can't be used (underscores are allowed)
        4. Variable names should be self-explanatory (you should be able to tell what the variable is for from the name)
        5. Keywords cannot be used (ex. #, using, namespace, etc.)
        6. Variables should be nouns
    */
}
